# 1. ARITHMETIC OPERATORS (Ganit wale kaam)
a = 2
b = 5
print("----- ARITHMETIC (Ganit) -----")
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} % {b} = {a % b} (Remainder - Baki bacha hua)")

# 2. RELATIONAL OPERATORS (Tulna karna)
x = 10
y = 20
print("\n----- RELATIONAL (Tulna) -----")
print(f"Is {x} equal to {y}? {x == y}")
print(f"Is {x} greater than {y}? {x > y}")
print(f"Is {x} less than {y}? {x < y}")

# 3. LOGICAL OPERATORS (And/Or/Not - Conditions ka khel)
a = True
b = False
print("\n----- LOGICAL (And/Or) -----")
print(f"a and b: {a and b}")  # Dono True hona chahiye
print(f"a or b: {a or b}")    # Koi ek True hona chahiye
print(f"not a: {not a}")      # Ulta kar do

# 4. ASSIGNMENT OPERATORS (Chhoti shorthand)
num = 5
print("\n----- ASSIGNMENT (Shorthand) -----")
num += 3  # Matlab num = num + 3
print(f"num += 3: {num}")
num *= 2  # Matlab num = num * 2
print(f"num *= 2: {num}")